// Affiliate cloaking. MAIN page (18 curated TVs) -> static public/_redirects (unchanged behavior).
// RECOMANDARI (1255 feed TVs) -> a Pages Function functions/out/[slug].js with an embedded
// slug->deeplink map (no 2,000-redirect _redirects limit). /out/<slug>/ stays the cloak URL on both.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const products = JSON.parse(readFileSync(fileURLToPath(new URL('../src/data/products.json', import.meta.url)), 'utf-8'));
const main = [...(products.ieftine || []), ...(products.buget || []), ...(products.premium || [])];

let redirects = '';
for (const p of main) {
  if (p.affiliate && p.slug) {
    redirects += `/out/${p.slug}/ ${p.affiliate} 302\n`;
    redirects += `/out/${p.slug} ${p.affiliate} 302\n`;
  }
}
// dropped products (no longer in the feed) -> 301 to a similar surviving product (mirrors the server).
// Static _redirects keeps live /tv/ pages pure-static (no per-request Function). 2,000-rule cap -> pruned at source.
let dropped = {};
try { dropped = JSON.parse(readFileSync(fileURLToPath(new URL('../.cache/dropped.json', import.meta.url)), 'utf-8')); } catch { /* none yet */ }
let dn = 0;
for (const [slug, target] of Object.entries(dropped)) {
  redirects += `/tv/${slug}/ ${target} 301\n`;
  redirects += `/tv/${slug} ${target} 301\n`;
  dn++;
}
const ruleCount = redirects.split('\n').length - 1;
if (ruleCount > 1800) console.warn(`  WARN: _redirects has ${ruleCount} rules (cap 2000) — lower RETAIN_DAYS in gen-products.`);
writeFileSync(fileURLToPath(new URL('../public/_redirects', import.meta.url)), redirects);
console.log(`_redirects: ${main.length} main /out + ${dn} dropped-product 301s (${ruleCount} rules)`);

let recs = [];
try { recs = JSON.parse(readFileSync(fileURLToPath(new URL('../src/data/recomandari.json', import.meta.url)), 'utf-8')); } catch { /* no recs yet */ }
const map = {};
// curated main /out too — the Function intercepts ALL /out/*, so curated slugs MUST be in the MAP (else fall to listing)
for (const p of main) if (p.affiliate && p.slug && !map[p.slug]) map[p.slug] = p.affiliate;
for (const p of recs) if (p.affiliate && p.slug && !map[p.slug]) map[p.slug] = p.affiliate;

mkdirSync(fileURLToPath(new URL('../functions/out', import.meta.url)), { recursive: true });
const fn = `// AUTO-GENERATED by scripts/generate-redirects.mjs — cloaked affiliate redirect for /out/<slug>/
const MAP = ${JSON.stringify(map)};
export function onRequest(context) {
  const url = MAP[context.params.slug];
  if (url) return Response.redirect(url, 302);
  return Response.redirect(new URL('/recomandari/', context.request.url).toString(), 302);
}
`;
writeFileSync(fileURLToPath(new URL('../functions/out/[slug].js', import.meta.url)), fn);
console.log(`functions/out/[slug].js: ${Object.keys(map).length} recomandari deeplinks`);
